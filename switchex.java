class switchex
{
 public static void main(String args[])
 {
   char c='N';
   switch(c)
   {
     case 'N':
	 System.out.println("value is a:");
	 break;
	 
	 case 'b':
	 System.out.println("value is b:");
	 break;
	 
	 case 'c':
	 System.out.println("value is c:");
	 break;
	 
	 default:
	 System.out.println("value invalid:");
	 break;
	 
	 }
	 }
}
	 